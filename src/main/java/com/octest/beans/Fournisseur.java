package com.octest.beans;

public class Fournisseur {
	
	private String id;
	private String nomSociete;
	
	public Fournisseur() {}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNomSociete() {
		return nomSociete;
	}
	public void setNomSociete(String nomSociete) {
		this.nomSociete = nomSociete;
	}

}
